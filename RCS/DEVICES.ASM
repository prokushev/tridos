; Check whether the mode is color or mono. Set EBX to base CRT (3x4) address.
	mov	ebx, V_MONO_CRT_ADDR
	mov	edx, V_MISC_IN
	in	al, dx
	mov	(VIDEO_CONTEXT PTR es:[edi]).MiscOutput, al

	and	eax, 1
	shl	eax, 5
	add	ebx, eax

	cld

; Save CRT registers.
	SAVE_BUNCH_REGS	ebx, V_CRT_REGS

; Save sequencer registers.
	SAVE_BUNCH_REGS	V_SEQ_ADDR, V_SEQ_REGS

; Save graphics controller registers.
	SAVE_BUNCH_REGS	V_GFX_ADDR, V_GFX_REGS

; Save attribute controller registers.
	mov	ecx, ebx
	add	ecx, V_CLR_STS1 - V_CLR_CRT_ADDR
	sub	ah, ah

save_attr_regs:
	mov	edx, ecx
	in	al, dx		; Reset address flip-flop.
	mov	edx, V_ATTR_ADDR

	mov	al, ah
	out	dx, al

	inc	edx
	in	al, dx

	stosb
	inc	ah
	cmp	ah, V_ATTR_REGS - 1
	jb	save_attr_regs
	ja	@@F
	or	ah, V_Attr_SCR_ENABLE
	jmp	save_attr_regs
@@@@:

; Save DAC registers.
	mov	edx, V_DAC_PEL_MASK
	in	al, dx
	stosb				; PEL mask reg.

	mov	edx, V_DAC_READ_ADDR
	sub	al, al
	out	dx, al

	mov	edx, V_DAC_DATA
	mov	ecx, V_DAC_REGS - 1
		rep	insb		; DAC RGB data.

	ret
SaveVideoContext	ENDP


;-----------------------------------------------------------------------------
;
;	Saves video memory (all bit planes).
;
;	I: ES:EDX -> task's structure.
;
; (!) Must be in the context of task being saved.
;
;-----------------------------------------------------------------------------
PUBLIC	SaveVideoMemory
SaveVideoMemory		PROC
	pushad

	mov	ebp, edx
; Set all video memory clocks to CPU interface.
	mov	edx, V_SEQ_ADDR
	mov	al, V_Seq_CLOCK_MODE
	out	dx, al
	inc	edx
	in	al, dx
	movzx	ebx, al
	shl	ebx, 16			; Keep clock mode.
	dec	edx
	mov	ah, al
	mov	al, V_Seq_CLOCK_MODE
	or	ah, 20h
	out	dx, ax			; Give all the time to CPU.

; Check memory mapping.
	mov	edx, V_GFX_ADDR
	mov	al, V_Gfx_MISC
	out	dx, al
	inc	edx
	in	al, dx
	test	al, 8			; Text modes?
	jnz	text_modes

; Graphics modes. Check whether current BIOS mode uses bitplanes on the same
; addresses or chains.
	mov	al, fs:[449h]
	cmp	al, 0Dh
	je	plane_modes
	cmp	al, 10h	
	je	plane_modes
	cmp	al, 12h
	je	plane_modes
	cmp	al, 0Eh
	je	plane_modes
	jb	cga_modes

; EGA/VGA chained modes.
	mov	esi, 0A0000h
	mov	eax, 0A0000h SHR 12
	mov	edx, (0A0000h + 10000h) SHR 12
	jmp	@@F

cga_modes:
	mov	esi, 0B8000h
	mov	eax, 0B8000h SHR 12
	mov	edx, (0B8000h + 10000h) SHR 12

@@@@:
	call	copy_mem
	jmp	gfx_exit

plane_modes:
; Set read mode 0.
	mov	edx, V_GFX_ADDR
	mov	al, V_Gfx_MODE
	out	dx, al
	inc	edx
	in	al, dx
	mov	bh, al			; Save graphics ctl. mode register.
	dec	edx
	mov	al, V_Gfx_MODE
	and	ah, 01100000b		; Read and write mode 0, no odd/even
					; mode.
	out	dx, ax

	mov	al, V_Gfx_BIT_PLANE_READ
	out	dx, al
	inc	edx
	in	al, dx
	mov	bl, al			; Save previous bit plane (BL).
	dec	edx

	sub	ah, ah			; AH = bit plane.
	mov	ecx, 0A0000h SHR 12

save_bit_plane:
	push	eax

	mov	edx, V_GFX_ADDR
	mov	al, V_Gfx_BIT_PLANE_READ
	out	dx, ax

	mov	esi, 0A0000h
	mov	eax, ecx
	mov	edx, ecx
	add	edx, 10000h SHR 12
	call	copy_mem
	pop	eax

	inc	ah
	add	ecx, 10000h SHR 12
	cmp	ah, 4
	jb	save_bit_plane

; Restore mode select.
	mov	edx, V_GFX_ADDR
	mov	al, V_Gfx_MODE
	mov	ah, bh
	out	dx, ax

; Restore bit plane select.
	mov	edx, V_GFX_ADDR
	mov	al, V_Gfx_BIT_PLANE_READ
	mov	ah, bl
	out	dx, ax

gfx_exit:
; Restore clock mode.
	shr	ebx, 16
	mov	edx, V_SEQ_ADDR
	mov	al, V_Seq_CLOCK_MODE
	mov	ah, bl
	out	dx, ax

	popad
	ret

text_modes:
	mov	TempMisc, al

; Text modes. Reprogram bit plane select to access plane 2/3.
	mov	edx, V_GFX_ADDR
	mov	al, V_Gfx_BIT_PLANE_READ
	out	dx, al
	inc	edx
	in	al, dx
	mov	bl, al			; Save previous bit plane (BL).
	dec	edx
	mov	al, V_Gfx_BIT_PLANE_READ
	mov	ah, 2			; Set bit plane 2.
	out	dx, ax

; Enable odd bit planes.
	mov	edx, V_SEQ_ADDR
	mov	al, V_Seq_MEM_MODE
	out	dx, al
	inc	edx
	in	al, dx
	dec	edx
	mov	TempMemMode, al
	mov	ah, al
; VGADOC4B has this one reversed. If set, disables odd bitplanes.
; Enable odd/even mode.
	and	ah, NOT 4
	mov	al, V_Seq_MEM_MODE
	out	dx, ax

; Reprogram memory mapping register.
	mov	edx, V_GFX_ADDR
	mov	al, V_Gfx_MISC
	mov	ah, TempMisc
	and	ah, NOT 00001100b	; Map memory to A0000 - BFFFF
	or	ah, 00000010b
	out	dx, ax

; Save character generator tables.
	mov	esi, 0A0000h
	mov	eax, 0A0000h SHR 12
	mov	edx, (0A0000h + 10000h) SHR 12
	call	copy_mem
	mov	esi, 0B0000h
	mov	eax, 0C0000h SHR 12
	mov	edx, (0C0000h + 10000h) SHR 12
	call	copy_mem
; Remap memory back.
	mov	edx, V_GFX_ADDR
	mov	al, V_Gfx_MISC
	mov	ah, TempMisc
	out	dx, ax

; Restore bit plane select (0 & 1).
	mov	edx, V_GFX_ADDR
	mov	al, V_Gfx_BIT_PLANE_READ
	mov	ah, bl
	out	dx, ax

; Restore sequencer's memory mode reg.
	mov	edx, V_SEQ_ADDR
	mov	al, V_Seq_MEM_MODE
	mov	ah, TempMemMode
	out	dx, ax

	test	TempMisc, 4		; CGA compatible modes?
	jz	mono_modes

	mov	esi, 0B8000h		; Save characters and attributes.
	mov	eax, 0B8000h SHR 12
	mov	edx, (0B8000h + 8000h) SHR 12
	call	copy_mem
	jmp	restore_clock

mono_modes:
	mov	esi, 0B0000h		; Save characters and attributes.
	mov	eax, 0B0000h SHR 12
	mov	edx, (0B0000h + 8000h) SHR 12
	call	copy_mem

restore_clock:
; Restore clock mode.
	shr	ebx, 16
	mov	edx, V_SEQ_ADDR
	mov	al, V_Seq_CLOCK_MODE
	mov	ah, bl
	out	dx, ax

	popad
	ret

copy_mem:
; Copy task's memory at A0000h - BFFFFh to virtual (take from task's
; alias mapping).
	push	ecx

	cld
save_page:

	mov	ecx, 400h			; Number of dwords in 1 page.
	mov	edi, (DosTask PTR es:[ebp]).TaskMapping
	mov	edi, es:[edi+eax*4]		; ES:EDI-> dest page
		rep	movs dword ptr es:[edi], es:[esi]

	inc	eax				; Next page
	cmp	eax, edx
	jb	save_page

	pop	ecx
	ret
SaveVideoMemory		ENDP


;-----------------------------------------------------------------------------
;
;	Restores video memory
;
;	I: ES:EDX -> task's structure.
;
; (!) Must be in the context of task being saved.
;
;-----------------------------------------------------------------------------
PUBLIC	RestoreVideoMemory
RestoreVideoMemory	PROC
	pushad

	mov	ebp, edx
; Set all video memory clocks to CPU interface.
	mov	edx, V_SEQ_ADDR
	mov	al, V_Seq_CLOCK_MODE
	out	dx, al
	inc	edx
	in	al, dx
	movzx	ebx, al
	shl	ebx, 16			; Keep clock mode.
	dec	edx
	mov	ah, al
	mov	al, V_Seq_CLOCK_MODE
	or	ah, 20h
	out	dx, ax			; Give all the time to CPU.

; Check memory mapping.
	mov	edx, V_GFX_ADDR
	mov	al, V_Gfx_MISC
	out	dx, al
	inc	edx
	in	al, dx
	test	al, 8			; Text modes?
	jnz	text_modes

; Graphics modes. Check whether current BIOS mode uses bitplanes on the same
; addresses or chains.
	mov	al, fs:[449h]
	cmp	al, 0Dh
	je	plane_modes
	cmp	al, 10h	
	je	plane_modes
	cmp	al, 12h
	je	plane_modes
	cmp	al, 0Eh
	je	plane_modes
	jb	cga_modes

; EGA/VGA chained modes.
	mov	edi, 0A0000h
	mov	eax, 0A0000h SHR 12
	mov	edx, (0A0000h + 10000h) SHR 12
	jmp	@@F

cga_modes:
	mov	edi, 0B8000h
	mov	eax, 0B8000h SHR 12
	mov	edx, (0B8000h + 10000h) SHR 12

@@@@:
	call	restore_mem
	jmp	gfx_exit

plane_modes:
; Set read mode 0.
	mov	edx, V_GFX_ADDR
	mov	al, V_Gfx_MODE
	out	dx, al
	inc	edx
	in	al, dx
	mov	bh, al			; Save graphics ctl. mode register.
	dec	edx
	mov	al, V_Gfx_MODE
	and	ah, 01100000b		; Read and write mode 0, no odd/even
					; mode.
	out	dx, ax

; Reprogram bit plane write select to access bit plane 0.
	mov	al, V_Seq_BIT_PLANE_WRITE
	out	dx, al
	inc	edx
	in	al, dx
	dec	edx
	mov	bl, al			; Save previous bit plane mask.

	mov	ah, 1			; Set bit plane 0.
	mov	ecx, 0A0000h SHR 12
restore_bit_plane:
	push	eax

	mov	edx, V_SEQ_ADDR
	mov	al, V_Seq_BIT_PLANE_WRITE
	out	dx, ax

; Copy video memory.
	mov	edi, 0A0000h
	mov	eax, ecx
	mov	edx, ecx
	add	edx, 10000h SHR 12
	call	restore_mem
	pop	eax

	add	ecx, 10000h SHR 12
	shl	ah, 1
	cmp	ah, 00010000b
	jb	restore_bit_plane

; Restore bit plane write enable.
	mov	edx, V_SEQ_ADDR
	mov	al,  V_Seq_BIT_PLANE_WRITE
	mov	ah, bl
	out	dx, ax

; Restore read/write mode.
	mov	edx, V_GFX_ADDR
	mov	al,  V_Gfx_MODE
	mov	ah, bh
	out	dx, ax

gfx_exit:
; Restore clock mode.
	shr	ebx, 16
	mov	edx, V_SEQ_ADDR
	mov	al, V_Seq_CLOCK_MODE
	mov	ah, bl
	out	dx, ax

	popad
	ret

text_modes:
	mov	TempMisc, al

; Save sequencer's memory mode register.
	mov	edx, V_SEQ_ADDR
	mov	al, V_Seq_MEM_MODE
	out	dx, al
	inc	edx
	in	al, dx
	dec	edx
	mov	TempMemMode, al
	mov	ah, al
; VGADOC4B has this one reversed. If set, disables odd bitplanes.
; Enable odd/even mode.
	and	ah, NOT 4
	mov	al, V_Seq_MEM_MODE
	out	dx, ax

; Reprogram bit plane write select to access bit plane 2.
	mov	al, V_Seq_BIT_PLANE_WRITE
	out	dx, al
	inc	edx
	in	al, dx
	dec	edx
	mov	bl, al			; Save previous bit plane mask.
	mov	al, V_Seq_BIT_PLANE_WRITE
	mov	ah, 4
	out	dx, ax			; Set bit plane 2.

; Set read and write mode 0. (EDX = V_GFX_ADDR).
	mov	al, V_Gfx_MODE
	out	dx, al
	inc	edx
	in	al, dx
	mov	bh, al			; Save previous mode (BH).
	dec	edx
	mov	al, V_Gfx_MODE
	sub	ah, ah			; Read mode 0.
	out	dx, ax			; Set read mode.

; Reprogram memory mapping register.
	mov	edx, V_GFX_ADDR
	mov	al, V_Gfx_MISC
	mov	ah, TempMisc
	and	ah, NOT 00001100b	; Map memory to A0000 - BFFFF
	or	ah, 00000010b
	out	dx, ax

; Restore character generator tables.
	mov	edi, 0A0000h
	mov	eax, 0A0000h SHR 12
	mov	edx, (0A0000h + 10000h) SHR 12
	call	restore_mem
	mov	edi, 0B0000h
	mov	eax, 0C0000h SHR 12
	mov	edx, (0C0000h + 10000h) SHR 12
	call	restore_mem

; Remap memory back to B8000 - BFFFF
	mov	edx, V_GFX_ADDR
	mov	al, V_Gfx_MISC
	mov	ah, TempMisc
	out	dx, ax

; Restore bit plane select (0 & 1).
	mov	edx, V_SEQ_ADDR
	mov	al, V_Seq_BIT_PLANE_WRITE
	mov	ah, bl
	out	dx, ax

; Restore mode register.
	mov	al, bh
	out	dx, ax

; Restore sequencer's memory mode reg.
	mov	edx, V_SEQ_ADDR
	mov	al, V_Seq_MEM_MODE
	mov	ah, TempMemMode
	out	dx, ax

	test	TempMisc, 4		; CGA compatible modes?
	jz	mono_modes

	mov	edi, 0B8000h		; Restore characters and attributes.
	mov	eax, 0B8000h SHR 12
	mov	edx, (0B8000h + 8000h) SHR 12
	call	restore_mem
	jmp	restore_clock

mono_modes:
	mov	edi, 0B0000h		; Restore characters and attributes.
	mov	eax, 0B0000h SHR 12
	mov	edx, (0B0000h + 8000h) SHR 12
	call	restore_mem

restore_clock:
; Restore clock mode.
	shr	ebx, 16
	mov	edx, V_SEQ_ADDR
	mov	al, V_Seq_CLOCK_MODE
	mov	ah, bl
	out	dx, ax

	popad
	ret

restore_mem:
; Copy task's memory at A0000h - BFFFFh from virtual (take from task's
; alias mapping).
	push	ecx

	cld
restore_page:
	mov	ecx, 400h			; Number of dwords in 1 page.
	mov	esi, (DosTask PTR es:[ebp]).TaskMapping
	mov	esi, es:[esi+eax*4]		; ES:ESI-> source page
		rep	movs dword ptr es:[edi], es:[esi]

	inc	eax				; Next page
	cmp	eax, edx
	jb	restore_page

	pop	ecx
	ret
RestoreVideoMemory	ENDP


;-----------------------------------------------------------------------------
;
;	Restores video context.
;
;	I: ES:ESI -> memory area with previously saved context.
;
;-----------------------------------------------------------------------------
PUBLIC	RestoreVideoContext
RestoreVideoContext	PROC	USES eax ebx ecx edx esi
; Restore misc. output register.
	mov	al, (VIDEO_CONTEXT PTR es:[esi]).MiscOutput
	mov	edx, V_MISC_OUT
	out	dx, al

; Check whether the mode is color or mono. Set EBX to base CRT (3x4) address.
	mov	ebx, V_MONO_CRT_ADDR
	mov	edx, V_MISC_IN
	in	al, dx
	and	eax, 1
	shl	eax, 5
	add	ebx, eax

	cld

; Restore CRT registers.

; Enable write to CRT registers 0-7.
	mov	edx, ebx
	mov	al, V_Crt_VERT_RETRACE_END
	out	dx, al
	inc	edx
	in	al, dx
	dec	edx
	and	al, 7Fh
	mov	ah, al
	mov	al, V_Crt_VERT_RETRACE_END
	out	dx, ax

	REST_BUNCH_REGS	ebx, V_CRT_REGS

; Restore sequencer registers.
	REST_BUNCH_REGS	V_SEQ_ADDR, V_SEQ_REGS

; Restore graphics controller registers.
	REST_BUNCH_REGS	V_GFX_ADDR, V_GFX_REGS

; Restore attribute controller registers.
	mov	edx, ebx
	add	edx, V_CLR_STS1 - V_CLR_CRT_ADDR
	in	al, dx		; Reset address flip-flop.
	sub	ah, ah
	mov	edx, V_ATTR_ADDR

restore_attr_regs:
	mov	al, ah
	out	dx, al

	lods	byte ptr es:[esi]
	out	dx, al

	inc	ah
	cmp	ah, V_ATTR_REGS - 1
	jb	restore_attr_regs
	ja	@@F
	or	ah, V_Attr_SCR_ENABLE
	jmp	restore_attr_regs
@@@@:

; Restore DAC registers.
	mov	edx, V_DAC_PEL_MASK
	lods	byte ptr es:[esi]	; PEL mask reg.
	out	dx, al

	mov	edx, V_DAC_WRITE_ADDR
	sub	al, al
	out	dx, al

	mov	edx, V_DAC_DATA
	mov	ecx, V_DAC_REGS - 1
		rep	outs dx, byte ptr es:[esi]	; DAC RGB data.

	ret
RestoreVideoContext	ENDP


