	cmp	eax, KBD_DATA
	je	write_data
	cmp	eax, KBD_STATUS
	jne	@@F

	mov	ch, cl
	and	ch, 0F0h
	cmp	ch, 0F0h
	jne	write_data
; Prevent program from booting the computer.
	or	cl, 1
	jmp	write_data
@@@@:
	stc
	ret

write_data:
	mov	edx, eax
	mov	al, cl
	out	dx, al

	clc
	ret
WriteKeyboardPort	ENDP


