;-----------------------------------------------------------------------------
;
;	I:  DX = PIC base address(20h=master, A0h=slave)
;	    AH = Start INT # for appropriate IRQ.
;
;	(!) AH must be a multiply of 8.
;
;	Initialize PIC. Done for remapping A3-A7 of INT vector.
;
;-----------------------------------------------------------------------------
PUBLIC	InitPIC
InitPIC		PROC	near
	mov	al, CMD_ICW1
	out	dx, al

	inc	dx
	mov	al, ah
	out	dx, al

	mov	al, CMD_ICW3
	cmp	dx, PIC_MASTER_MASK
	je	put_icw3
	mov	al, CMD_SLAVE_ICW3
put_icw3:
	out	dx, al

	mov	al, CMD_ICW4
	out	dx, al
	ret
InitPIC		ENDP


;-----------------------------------------------------------------------------
;
;	Set up video mode (text) and base address (mono/color).
;
;-----------------------------------------------------------------------------
PUBLIC	SetupVideo
SetupVideo	PROC	near
; Detect CRT type.
	call	DetectCrt
	mov	CrtId, al

; Set base CRT controller and ports addresses.
	mov	CrtBase, 3B0h
	mov	VideoTxtBuf, OS_VIDEO_BUF + 10000h
	mov	VideoGfxBuf, OS_VIDEO_BUF + 10000h
	cmp	al, 1
	jna	crt_set
	add	CrtBase, 20h	; Set CRT to 3D0h.
	mov	VideoTxtBuf, OS_VIDEO_BUF + 18000h
	mov	VideoGfxBuf, OS_VIDEO_BUF
crt_set:
; Allocate GDT selectors for text and graphics video buffers.
	ADD_GDT_SEGMENT	VideoTxtBuf, 7FFFh, DATA_ACCESS
	mov	VBufTextSel, ax
	ADD_GDT_SEGMENT	VideoGfxBuf, 0FFFFh, DATA_ACCESS
	mov	VBufGfxSel, ax

; Set mode 3.
	mov	ax, 3
	int	10h

	cmp	CrtId, 5
	jnb	@@F
	call	InitErrHandler

@@@@:
	ret
SetupVideo	ENDP


;-----------------------------------------------------------------------------
;
;	I:
;	O:  AL = 1: mono
;		 2: CGA
;		 3: EGA
;		 4: MCGA
;		 5: VGA/SVGA
;
;	Detects video adapter according to BIOS video modes that it supports.
;
;-----------------------------------------------------------------------------
PUBLIC	DetectCrt
DetectCrt	PROC	near
	sub	ax, ax
	int	10h
	mov	ah, 0Fh
	int	10h
	test	al, al
	jz	col
	mov	al, 1
	ret
col:
	mov	ax, 13h
	int	10h
	mov	ah, 0Fh
	int	10h
	cmp	al, 13h
	jz	mcgavga
	mov	ax, 0Dh
	int	10h
	mov	ah, 0Fh
	int	10h
	cmp	al, 0Dh
	jz	ega
	mov	al, 2
	ret
ega:
	mov	al, 3
	ret
mcgavga:
	mov	ax, 0Dh
	int	10h
	mov	ah, 0Fh
	int	10h
	cmp	al, 0Dh
	mov	al, 4
	jnz	to_ret
	inc	ax
to_ret:
	ret
DetectCrt	ENDP


;-----------------------------------------------------------------------------
;
; Procedure BEEPs on speaker
;
;-----------------------------------------------------------------------------
Beep		PROC
	push	ax
	push	bx
	push	cx

	in	al, KBD_PORT_B
	push	ax
	mov	cx, 100h
Beep0:
	push	cx
	and	al, 11111100b
	out	KBD_PORT_B, al
	mov	cx, 0C0h
	loop	$
	or	al, 00000010b
	out	KBD_PORT_B, al
	mov	cx, 0C0h
	loop	$
	pop	cx
	loop	Beep0

	pop	ax
	out	KBD_PORT_B, al

	pop	cx
	pop	bx
	pop	ax
	ret
Beep		ENDP


