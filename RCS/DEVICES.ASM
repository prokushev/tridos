PUBLIC	GotoXy
GotoXy		PROC	near32	USES eax ecx edx
	mov	CursorX, al
	mov	CursorY, ah

	mov	edx, V_CLR_CRT_ADDR
	mov	al, ah
	mul	Columns
	add	al, CursorX
	adc	ah, 0
	mov	ecx, eax

	mov	al, V_Crt_CURSOR_ADDR_LSB	; CRT cursor position LSB index
	mov	ah, cl
	out	dx, ax

	mov	al, V_Crt_CURSOR_ADDR_MSB	; CRT cursor position MSB index
	mov	ah, ch
	out	dx, ax

	ret
GotoXy		ENDP


