;-----------------------------------------------------------------------------
;
;	Writes a DMA port.
;
;	I: EAX = port
;	   CL = value.
;	O: CF = 0	-	OK
;	        1	-	error (wrong port).
;
;-----------------------------------------------------------------------------
WriteDMAPort	PROC	USES eax ebx ecx edx esi edi
	cmp	eax, 20h
	jb	dma_port
	cmp	eax, 80h
	jb	not_dma_port
	cmp	eax, 8Fh
	ja	not_dma_port

dma_port:
	cmp	eax, DMA1_MASK
	jne	is_dma1_addr_clear?

; A DMA 1 channel is being programmed.
	test	cl, 00000100b
	jnz	do_write

; Unmask virtual register and start programming. CL holds the channel to program.
	mov	DmaMask, cl
	and	cl, 00000011b

;	mov	al, 1
;	shl	al, cl
;	not	al
;	and	DmaMaskReg, al

	mov	ch, DmaMode
	and	ch, 00001100b

; If mode is write to memory, setup DMA range and do the copy later.
	movzx	edx, cl
	cmp	ch, 00000100b
	jne	read_from_mem

	mov	eax, Dma1BaseAddr[ edx * 4 ]
	mov	ebx, CurrentTask
	mov	DmaIoRange[ ebx * 8 ], eax
	mov	eax, Dma1Count[ edx * 4 ]
	inc	eax
	mov	DmaIoRange[ ebx * 8 ][ 4 ], eax
	mov	RetireDmaFlag, 1
	jmp	program_addr
;	jmp	do_unmask

; If mode is read from memory, copy memory to DMA buffer and go on.
read_from_mem:
	push	es
	push	ecx

	push	fs
	pop	es
	mov	esi, Dma1BaseAddr[ edx * 4 ]
	mov	edi, DmaBufAddr
	add	edi, OS_1ST_MB
	cld
	mov	ecx, Dma1Count[ edx * 4 ]
	inc	ecx
		rep	movs byte ptr es:[edi], es:[esi]

	pop	ecx
	pop	es

program_addr:
; Program the addresses.
	movzx	edx, cl
	mov	eax, DmaBufAddr
	shl	edx, 1		; EDX -> base address for appropriate channel.
	out	dx, al
	IODelay
	xchg	al, ah
	out	dx, al
	IODelay
	shr	eax, 16
	mov	edx, Dma1PageAddress[ edx * 2 ]
	out	dx, al
	IODelay

	movzx	edx, cl
	mov	eax, Dma1Count[ edx * 4 ]
	shl	edx, 1
	inc	edx
	out	dx, al
	IODelay
	xchg	al, ah
	out	dx, al
	IODelay

;	clc
;	ret

do_unmask:
; Unmask the channel.
	mov	al, DmaMask
	out	DMA1_MASK, al
; Clear the internal flipflops.
;	mov	Dma1FlipFlops, 0
	clc
	ret

set_mask1:
	jmp	do_write
;	and	cl, 00000011b
;	mov	al, 1
;	shl	al, cl
;	or	DmaMaskReg, al
;	clc
;	ret
	mov	Dma1FlipFlops, 0
	
is_dma1_addr_clear?:
; If a flip flop is cleared, clear it.
	cmp	eax, DMA1_ADDR_CLEAR
	jne	is_dma1_mode?
	mov	Dma1FlipFlops, 0
	jmp	do_write
;	clc
;	ret

is_dma1_mode?:
	cmp	eax, DMA1_MODE
	jne	is_dma1_addr?
	mov	DmaMode, cl
	jmp	do_write

is_dma1_addr?:
; If an address or count register is programmed.
	cmp	eax, DMA1_CMD
	jnb	is_dma1_page?

	test	eax, 1		; Just bypass count registers
	jnz	get_count

; Write address registers.
	mov	edx, eax
	shr	edx, 1
	bt	Dma1FlipFlops, edx
	jc	@@F

	mov	byte ptr Dma1BaseAddr[ edx * 4 ], cl
	bts	Dma1FlipFlops, edx
	clc
	ret
@@@@:
	mov	byte ptr Dma1BaseAddr[ edx * 4 ][ 1 ], cl
	btc	Dma1FlipFlops, edx
	clc
	ret

get_count:
	push	eax
	push	edx

;	mov	edx, eax
;	mov	al, cl
;	out	dx, al

	pop	edx
	pop	eax

	mov	edx, eax
	shr	edx, 1
	mov	eax, edx
	add	edx, 4
	bt	Dma1FlipFlops, edx
	jc	@@F

	mov	byte ptr Dma1Count[ eax * 4 ], cl
	bts	Dma1FlipFlops, edx
	clc
	ret

@@@@:
	mov	byte ptr Dma1Count[ eax * 4 ][ 1 ], cl
	btc	Dma1FlipFlops, edx
	clc
	ret

is_dma1_page?:
	cmp	eax, DMA_CH0_PAGE
	jne	@@F
	mov	byte ptr Dma1BaseAddr[ 2 ], cl
	clc
	ret

@@@@:
	cmp	eax, DMA_CH1_PAGE
	jne	@@F
	mov	byte ptr Dma1BaseAddr[ 4 ][ 2 ], cl
	clc
	ret

@@@@:
	cmp	eax, DMA_CH2_PAGE
	jne	@@F
	mov	byte ptr Dma1BaseAddr[ 8 ][ 2 ], cl
	clc
	ret

@@@@:
	cmp	eax, DMA_CH3_PAGE
	jne	do_write
	mov	byte ptr Dma1BaseAddr[ 12 ][ 2 ], cl
	clc
	ret

do_write:
	mov	edx, eax
	mov	al, cl
	out	dx, al
	clc
	ret

not_dma_port:
	stc
	ret
WriteDMAPort	ENDP


